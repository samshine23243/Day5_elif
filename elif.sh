echo "Enter number"
read Num
if [ $Num -eq "1" ]
then
   echo "Unit"
elif [ $NUM -eq '10' ]
then
   echo "ten"
elif [ $Num -eq "100" ]
then
   echo "Hundred"
elif [ $Num -eq "1000" ]
then
   echo "Thousand"
elif [ $Num -eq "10000" ]
then
   echo "Ten Thousand"
elif [ $Num -eq "100000" ]
then
   echo "Lakh"
fi

